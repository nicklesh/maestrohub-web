def replace_values_recursively(data, translations, locale_code):
    """Recursively replace English values with translations in a nested dict/list"""
    if isinstance(data, dict):
        for key, value in data.items():
            if isinstance(value, str):
                # Check if the value is in our translation map (for exact string matches)
                if value in translations:
                    data[key] = translations[value]
                # Check if the key has a translation (for key-based translations)
                elif key in translations and isinstance(translations.get(key), str):
                    data[key] = translations[key]
            else:
                replace_values_recursively(value, translations, locale_code)
    elif isinstance(data, list):
        for i, item in enumerate(data):
            if isinstance(item, dict) or isinstance(item, list):
                replace_values_recursively(item, translations, locale_code)

def update_locale_file(locale_code, translations):
    """Update a specific locale file replacing all matching values"""
    file_path = os.path.join(LOCALES_DIR, f"{locale_code}.json")
    
    if not os.path.exists(file_path):
        print(f"Warning: {file_path} does not exist")
        return
    
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Replace all matching values recursively
    replace_values_recursively(data, translations, locale_code)
    
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"Updated {locale_code}.json")

def main():
    print("Applying final comprehensive translations...")
    
    for locale_code, translations in FULL_TRANSLATIONS.items():
        update_locale_file(locale_code, translations)
    
    print("\nDone! All locale files have been updated.")

if __name__ == "__main__":
    main()
